package exemplos_cap2_deitel;

public class Welcome4 {
	
	public static void main(String[] args) {
		
		System.out.printf("%s%n%s%n", "Welcome to", "Java Programming"); 
		
		/* printf é uma string de formato que pode consistir em texto dixo e especificadores de formato.
		 * Cada especificador de formato é um marcador de lugar para um valor e especifica o tipo da saída de dados
		 * 
		 * %d - Valor inteiro
		 * %s - String
		 * %f - Float
		 * %x - Hexadecimal
		 * %c - Char
		 * %o - Octal
		 * %n - especificador de formato especial que, em vez de imprimir algo, faz com que printf() carregue a variável 
		 * apontada pelo argumento correspondente com um valor igual ao número de caracteres que foram impressos por printf() 
		 * antes do ocorrência de %n.*/
	}

}
