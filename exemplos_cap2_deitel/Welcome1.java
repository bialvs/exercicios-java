package exemplos_cap2_deitel;

public class Welcome1 { //Todo programa Java consistem em pelo menos uma classe. A palavra class introduz uma declaração de classe e é imediatamente seguida pelo seu nome
	
	public static void main(String[] args) { //O método MAIN inicia a execução do aplicativo Java. A palavra void significa que o método não retornará nenhuma informação
		
		System.out.println("Welcome to Java Programming!"); // imprime a mensagem na tela de comando. O objeto System.out é um objeto de saída padrão. O ' ; ' quer indicar uma instrução
		
	} // fim do método main

} // fim da classe Welcome1
